#\t = tab
tabby_cat = "\tI'm tabbed in."
#\n = new line
persian_cat = "I'm split\non a line."
#escaping other characters results in them displaying normally
backslash_cat = "I'm \\ a \\ cat."

#triple quotes is multiple line supported
fat_cat = """
I'll do a list:
\t* Cat Food
\t* Fishies
\t* Catnip\n\t* Grass
"""

#printing all the cats
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
